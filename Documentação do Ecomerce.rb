Documentação do Ecomerce

Casos de Uso - Curso de Ecomerce com NodeJS e React

Quem são os usuario do sistema?
Administrador
    Acesso a todos os pedidos feitos dentro da loja
        Alterar status dos pedidos e avisar os clientes dessa alteração pelo email
    
    Colocar o codigo de rastreamento para o clientes
        Alterar excluir e alterar pedido de cliente e avisar os clientes dessa alteração por email

    Ter acesso a todos os cliente que já pediram na loja
        Pesquisar por nome ou telefone na lis tagem de cliente
        Alterar os dados ou remover o cliente da loja
        Ter acesso a todos os pedidos feitos por um cliente
    
    Ter acesso a uma lista de produtos que estão disponiveis e indisponiveis na loja
        Alterar os dados de um produto - disponibilidade, preço, colocar em prodtudo
        Adicionar fotos para um produto
        Excluir produtos do sistema

    Ter acesso a uma lista com todas categorias que estão disponiveis e indisponiveis
        Aletrar os dados de uma categoria, deixar indisponivel e excluir categoria

    Acesso a uma lista de variações de um deteerminado produto
        Deixar indisponivel determinada variação 
        Enviar fotos excluivas para uma variação
        Alterar os dados da variação - colocar desconto, aterar preço base
    
    Acesso a uma lista com todas as avaliações de um determinado produto
        Conseguir responder as avaliações ou excluir excluivas

    Alterar os próprios dados do usuário
        Fazer login com administrador no sistema
        Recuperar a senha do seu cadastro direto pelo email
        Alterar os dados relacionados a loja
        Colocar loja em manutenção, alterar dados de telefone, alterar email, endereço 
    
Cliente
    Consegui deixar avaliações
    Alterar e excluir avaliações deixadas no produtos

    Fechar um pedido coompleto com meio de pagamento
    Realizar o pagamento diretamente pela loja
        Utilizando cartão de credito ou boleto
    
    Ver todos os pedidos que fez em seu nome
    Ver todos os detalhes de um pedido feito - pagamento, entrega dados da loja

    Conseguir cancelar o seu pedido
    Alterar seus dados pessoais e de entrega online

Visitante
    Ver todas as categoria disponiveis no sistema
    Ver todos os produtos de uma determinada categoria
    Ver todos detalhes de um determinado produto
    Ver cada variação disponivel para determinado produto
    Ver avaliações deixadas para cada produto selecionado
    Ver valores e descontos para cada produto na listagem

    Pesquisar um produto por um nome, pela descrição ou categoria
    Ver uma lista com vários produtos sortidos
    Conseguir alterar a ordem dos produtos, ordem alfabetica, ordem de preço, ordem aleatória

    Conseguir calcular o valor de entrega de determinado produto e do carrinho completo
    Conseguir também se cadastrar no sistema - fazer login, se cadastrar
    Ver as paginas institucionais da loja


